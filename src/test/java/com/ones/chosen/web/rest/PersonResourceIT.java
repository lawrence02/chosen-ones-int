    @Test
    @Transactional
    void putNewPerson() throws Exception {
        // Initialize the database
        personRepository.saveAndFlush(person);

        int databaseSizeBeforeUpdate = personRepository.findAll().size();

        // Update the person
        Person updatedPerson = personRepository.findById(person.getId()).get();
        // Disconnect from session so that the updates on updatedPerson are not directly saved in db
        em.detach(updatedPerson);
        updatedPerson
            .firstname(UPDATED_FIRSTNAME)
            .lastname(UPDATED_LASTNAME)
            .dob(UPDATED_DOB)
            .gender(UPDATED_GENDER)
            .occupation(UPDATED_OCCUPATION)
            .phone(UPDATED_PHONE)
            .baptized(UPDATED_BAPTIZED)
            .maritalStatus(UPDATED_MARITAL_STATUS)
            .nationality(UPDATED_NATIONALITY)
            .ministry(UPDATED_MINISTRY);

        restPersonMockMvc
            .perform(
                put(ENTITY_API_URL_ID, updatedPerson.getId())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(TestUtil.convertObjectToJsonBytes(updatedPerson))
            )
            .andExpect(status().isOk());

        // Validate the Person in the database
        List<Person> personList = personRepository.findAll();
        assertThat(personList).hasSize(databaseSizeBeforeUpdate);
        Person testPerson = personList.get(personList.size() - 1);
        assertThat(testPerson.getFirstname()).isEqualTo(UPDATED_FIRSTNAME);
        assertThat(testPerson.getLastname()).isEqualTo(UPDATED_LASTNAME);
        assertThat(testPerson.getDob()).isEqualTo(UPDATED_DOB);
        assertThat(testPerson.getGender()).isEqualTo(UPDATED_GENDER);
        assertThat(testPerson.getOccupation()).isEqualTo(UPDATED_OCCUPATION);
        assertThat(testPerson.getPhone()).isEqualTo(UPDATED_PHONE);
        assertThat(testPerson.getBaptized()).isEqualTo(UPDATED_BAPTIZED);
        assertThat(testPerson.getMaritalStatus()).isEqualTo(UPDATED_MARITAL_STATUS);
        assertThat(testPerson.getNationality()).isEqualTo(UPDATED_NATIONALITY);
        assertThat(testPerson.getMinistry()).isEqualTo(UPDATED_MINISTRY);
    }

